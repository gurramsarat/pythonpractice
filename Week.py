week=int(input("Enter a number "))
if(week==1):
    print("monday")
elif(week==2):
    print("tuesday")
elif(week==3):
    print("wednesday")
elif(week==4):
    print("Thursday")
elif(week==5):
    print("friday")
elif(week==6):
    print("saturday")
elif(week==7):
    print("sunday")
else:
    print("Invalid")
